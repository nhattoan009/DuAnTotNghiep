    public void insert() {
        Phong sv = getModel();
        try {
            dao.Insert(sv);
            this.clear();
            lblMessage.setText("Thêm mới thành công! Vui lòng Làm mới bản");

        } catch (Exception e) {
            lblMessage.setText("Thêm mới thất bại!");
            lblMessage.setForeground(Color.RED);
            System.out.print(e);
        }
//        } else {
//            lblMessage.setText("Lỗi!");
//        }
    }
    
    public void update() {
        Phong sv = getModel();
        try {
            dao.Update(sv);
            lblMessage.setText("Cập nhật thành công!");
        } catch (Exception e) {
            lblMessage.setText("Cập nhật không thành công!");
        }

    }

